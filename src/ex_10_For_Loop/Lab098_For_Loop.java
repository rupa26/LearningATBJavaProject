package ex_10_For_Loop;

public class Lab098_For_Loop {
    public static void main(String[] args) {
        //for Loop
        //Helps to repeast a block of code
        //initialization -> Condition  -> Upadtion(Incre/decre)
        //ICU

        for (int i = 0; i < 10; i++) {
            System.out.println(i);
            //0,1,2,3........9-  times it got executed 10 times(0-9)

        }
    }
}
